package com.slyvr.chat.style;

/**
 * Represents different states of alignments for chat texts.
 *
 * @since 1.0.0
 */
public enum TextAlignment {

    /**
     * Aligns text to the left side of the container.
     */
    LEFT,

    /**
     * Aligns text to the center of the container.
     */
    CENTER,

    /**
     * Aligns text to the right side of the container.
     */
    RIGHT

}
